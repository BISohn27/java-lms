create table course (
    id bigint generated by default as identity,
    title varchar(255) not null,
    creator_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table image (
       id bigint generated by default as identity,
       size int not null,
       type varchar(4) not null,
       width int not null,
       height int not null,
       created_at timestamp not null,
       updated_at timestamp,
       primary key (id)
);

create table session (
    id bigint generated by default as identity,
    course_id bigint not null,
    image_id bigint,
    start_date timestamp not null,
    end_date timestamp not null,
    pay_type varchar(4) not null,
    session_state varchar(10) not null,
    recruitment_state varchar(20) not null default 'NOT_RECRUITING',
    max_student int,
    fee bigint null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    foreign key (course_id) references course(id),
    foreign key (image_id) references image(id)
);

create table session_image (
       id bigint generated by default as identity,
       session_id bigint not null,
       image_id bigint not null,
       created_at timestamp not null,
       updated_at timestamp,
       primary key (id),
       foreign key (session_id) references session(id),
       foreign key (image_id) references image(id)
);

create table ns_user (
    id bigint generated by default as identity,
    user_id varchar(20) not null,
    password varchar(20) not null,
    name varchar(20) not null,
    email varchar(50),
    `authorization` varchar(10) default 'STUDENT',
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id)
);

create table payment (
    id bigint generated by default as identity,
    amount bigint not null,
    session_id bigint not null,
    user_id bigint not null,
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    foreign key (session_id) references session(id),
    foreign key (user_id) references ns_user(id)
);

create table study_student (
    id bigint generated by default as identity,
    session_id bigint not null,
    student_id bigint not null,
    selection_status varchar(10) default 'WAITING',
    created_at timestamp not null,
    updated_at timestamp,
    primary key (id),
    foreign key (session_id) references session(id),
    foreign key (student_id) references ns_user(id)
);

create table question (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    title varchar(100) not null,
    writer_id bigint,
    primary key (id)
);

create table answer (
    id bigint generated by default as identity,
    created_at timestamp not null,
    updated_at timestamp,
    contents clob,
    deleted boolean not null,
    question_id bigint,
    writer_id bigint,
    primary key (id)
);

create table delete_history (
    id bigint not null,
    content_id bigint,
    content_type varchar(255),
    created_date timestamp,
    deleted_by_id bigint,
    primary key (id)
);
